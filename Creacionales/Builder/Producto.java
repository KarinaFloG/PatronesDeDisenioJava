public class Producto {

    private String parteUno;
    private String parteDos;
    private String parteTres;

    public String getParteUno() {
        return parteUno;
    }

    public void setParteUno(String parteUno) {
        this.parteUno = parteUno;
    }

    public String getParteDos() {
        return parteDos;
    }

    public void setParteDos(String parteDos) {
        this.parteDos = parteDos;
    }

    public String getParteTres() {
        return parteTres;
    }

    public void setParteTres(String parteTres) {
        this.parteTres = parteTres;
    }
}